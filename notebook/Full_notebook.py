import cv2, os

VIDEO_DIR = '/content/drive/MyDrive/Dataset'        # your videos live here
FRAME_DIR = '/content/drive/MyDrive/Dataset_Frames' # frames will go here
EVERY_N   = 10  # save 1 frame every 10 frames (~3 per second for 30fps video)

classes = ['sports', 'news', 'movies', 'violence']

def extract_frames(video_path, output_dir, every_n=10):
    os.makedirs(output_dir, exist_ok=True)
    cap = cv2.VideoCapture(video_path)
    count, saved = 0, 0
    while True:
        ret, frame = cap.read()
        if not ret:
            break
        if count % every_n == 0:
            cv2.imwrite(f"{output_dir}/frame_{saved:05d}.jpg", frame)
            saved += 1
        count += 1
    cap.release()
    return saved

total_frames = 0
for cls in classes:
    vid_folder   = os.path.join(VIDEO_DIR, cls)
    frame_folder = os.path.join(FRAME_DIR, cls)
