import torch
from torchvision import datasets, transforms
from torch.utils.data import DataLoader, random_split

FRAME_DIR = '/content/Dataset_Frames'

transform = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.RandomHorizontalFlip(),
    transforms.ColorJitter(brightness=0.2, contrast=0.2),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406],
                         [0.229, 0.224, 0.225])
])

full_dataset = datasets.ImageFolder(FRAME_DIR, transform=transform)

print("Classes detected:", full_dataset.classes)
print("Total frames   :", len(full_dataset))

# 80% train, 20% validation
train_size = int(0.8 * len(full_dataset))
val_size   = len(full_dataset) - train_size
train_ds, val_ds = random_split(full_dataset, [train_size, val_size])

train_loader = DataLoader(train_ds, batch_size=32, shuffle=True,  num_workers=0)
val_loader   = DataLoader(val_ds,   batch_size=32, shuffle=False, num_workers=0)

print(f"\nTrain samples : {train_size}")
print(f"Val samples   : {val_size}")
print("DataLoaders ready!")

import torch.nn as nn
from torchvision import models

NUM_CLASSES = 4
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("Device:", device)  # should say 'cuda' if GPU is on

model = models.googlenet(pretrained=True, aux_logits=True)

# Freeze all pretrained layers
for param in model.parameters():
    param.requires_grad = False

# Replace the 3 output heads with 4-class versions
model.fc      = nn.Linear(1024, NUM_CLASSES)
model.aux1.fc2 = nn.Linear(1024, NUM_CLASSES)
model.aux2.fc2 = nn.Linear(1024, NUM_CLASSES)

# Unfreeze only the new heads
for head in [model.fc, model.aux1.fc2, model.aux2.fc2]:
    for param in head.parameters():
        param.requires_grad = True

model = model.to(device)
print("GoogLeNet loaded and ready!")

import torch
import torch.nn as nn
import torch.optim as optim

criterion = nn.CrossEntropyLoss()

optimizer = optim.Adam(
    filter(lambda p: p.requires_grad, model.parameters()),
    lr=1e-3
)

scheduler = optim.lr_scheduler.StepLR(
    optimizer,
    step_size=5,
    gamma=0.5
)

# ---------------------------
# TRAIN
# ---------------------------
def train_epoch(model, loader):
    model.train()

    total_loss = 0.0
    correct = 0
    total = 0

    for imgs, labels in loader:
        imgs = imgs.to(device, non_blocking=True)
        labels = labels.to(device, non_blocking=True)

        optimizer.zero_grad(set_to_none=True)

        out = model(imgs)
        logits = out.logits if hasattr(out, "logits") else out

        loss = criterion(logits, labels)
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

        preds = logits.argmax(dim=1)
        correct += (preds == labels).sum().item()
        total += labels.size(0)

    return total_loss / len(loader), correct / total


# ---------------------------
# EVALUATE
# ---------------------------
def evaluate(model, loader):
    model.eval()

    correct = 0
    total = 0

    with torch.no_grad():
        for imgs, labels in loader:
            imgs = imgs.to(device, non_blocking=True)
            labels = labels.to(device, non_blocking=True)

            out = model(imgs)
            logits = out.logits if hasattr(out, "logits") else out

            preds = logits.argmax(dim=1)
            correct += (preds == labels).sum().item()
            total += labels.size(0)

    return correct / total


# ---------------------------
# TRAIN LOOP
# ---------------------------
EPOCHS = 10

print(f"{'Epoch':>6} | {'Loss':>8} | {'Train Acc':>10} | {'Val Acc':>8}")
