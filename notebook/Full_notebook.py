import cv2
from PIL import Image
from collections import Counter

def predict_video(video_path, model, transform, class_names, every_n=10):
    cap = cv2.VideoCapture(video_path)
    predictions, count = [], 0
    model.eval()
    while True:
        ret, frame = cap.read()
        if not ret: break
        if count % every_n == 0:
            img    = Image.fromarray(cv2.cvtColor(frame, cv2.COLOR_BGR2RGB))
            tensor = transform(img).unsqueeze(0).to(device)
            with torch.no_grad():
                out    = model(tensor)
                logits = out.logits if hasattr(out, 'logits') else out
                predictions.append(class_names[logits.argmax(1).item()])
        count += 1
    cap.release()

    most_common = Counter(predictions).most_common(1)[0]
    print(f"\nVideo: {video_path}")
    print(f"Scene: {most_common[0].upper()}  ({most_common[1]}/{len(predictions)} frames agree)")
    print("\nFrame-by-frame breakdown:")
    for cls, cnt in Counter(predictions).most_common():
        bar = '█' * int(cnt / len(predictions) * 30)
        print(f"  {cls:<10} {bar} {cnt}")

