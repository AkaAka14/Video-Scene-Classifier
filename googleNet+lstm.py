def train_model(train_loader, val_loader, model, optimizer, criterion, epochs, device, save_path="best_model.pth"):
    best_acc = 0.0
    for epoch in range(epochs):
        model.train()
        train_correct, train_total = 0, 0
        for frames, labels in tqdm(train_loader, desc=f"Epoch {epoch+1} Training"):
            frames, labels = frames.to(device), labels.to(device)
            optimizer.zero_grad()
            outputs = model(frames)
            loss = criterion(outputs, labels)
            loss.backward()
            optimizer.step()
            _, preds = torch.max(outputs, 1)
            train_total += labels.size(0)
            train_correct += (preds == labels).sum().item()

        model.eval()
        val_correct, val_total = 0, 0
        with torch.no_grad():
            for frames, labels in tqdm(val_loader, desc=f"Epoch {epoch+1} Validation"):
                frames, labels = frames.to(device), labels.to(device)
                outputs = model(frames)
                _, preds = torch.max(outputs, 1)
                val_total += labels.size(0)
                val_correct += (preds == labels).sum().item()

        train_acc = 100 * train_correct / train_total
        val_acc = 100 * val_correct / val_total
        print(f"Epoch {epoch+1}/{epochs} | Train Acc: {train_acc:.2f}% | Val Acc: {val_acc:.2f}%")

        if val_acc > best_acc:
            best_acc = val_acc
            torch.save(model.state_dict(), save_path)
            print(f"  -> Saved best model (val acc {val_acc:.2f}%)")
    print(f"Best validation accuracy: {best_acc:.2f}%")
    return model

