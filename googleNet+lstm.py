class SceneClassifier(nn.Module):
    def __init__(self, hidden_size=256, num_layers=2):
        super().__init__()
        # GoogLeNet with pretrained weights (if internet available)
        try:
            self.backbone = models.googlenet(
                weights=models.GoogLeNet_Weights.IMAGENET1K_V1,
                aux_logits=False
            )
        except:
            print("Pretrained weights not available, using random init.")
            self.backbone = models.googlenet(weights=None, aux_logits=False)
        self.backbone.fc = nn.Identity()   # remove final layer -> 1024-dim features
        # Freeze backbone (train only LSTM + classifier)
        for param in self.backbone.parameters():
            param.requires_grad = False
        self.lstm = nn.LSTM(1024, hidden_size, num_layers, batch_first=True, dropout=0.5)
        self.classifier = nn.Linear(hidden_size, NUM_CLASSES)

    def forward(self, x):
        b, s, c, h, w = x.shape
        x = x.view(b * s, c, h, w)
        features = self.backbone(x)        # (b*s, 1024)
        features = features.view(b, s, -1) # (b, s, 1024)
        lstm_out, _ = self.lstm(features)
        return self.classifier(lstm_out[:, -1, :])

